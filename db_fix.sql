create policy "Allow public insert of notes" on public.notes for insert with check (true);
create policy "Allow public update of notes" on public.notes for update using (true);
create policy "Allow public delete of notes" on public.notes for delete using (true);

create policy "Allow public read of progress" on public.reading_progress for select using (true);
create policy "Allow public insert of progress" on public.reading_progress for insert with check (true);
create policy "Allow public update of progress" on public.reading_progress for update using (true);

